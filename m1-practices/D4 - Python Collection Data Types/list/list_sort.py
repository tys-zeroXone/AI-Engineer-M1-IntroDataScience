list_contoh = ["hello", "world", "andi"]
list_contoh.sort() # mengurutkan dari a-z
print(list_contoh)

list_contoh = [0, 3, 2, 1]
list_contoh.sort() # mengurutkan dari kecil ke besar
print(list_contoh)

list_contoh.sort(reverse=True) # mengurutkan dari besar ke kecil karena ada reverse=True
print(list_contoh)